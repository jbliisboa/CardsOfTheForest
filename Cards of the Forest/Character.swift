
class Character{
    
    var name:String?
    var experience:Int?
    var agility:Int?
    var force:Int?
    var image:String?
    
    init(_ name:String, _ experience:Int, _ agility:Int, _ force:Int, _ image:String)
    {
        
        self.name = name
        self.experience = experience
        self.agility = agility
        self.force = force
        self.image = image
        
    }
}
